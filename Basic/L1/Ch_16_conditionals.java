
public class Ch_16_conditionals {
    public static void main(String[] args) {
        
        /*  Condition - if and else and their syntax.
            If and else trigger on boolean values, for true is if and for false is else.

            Relation operator like - <, >, ==   returns a boolean value after evaluating.
            like - 14 > 3, the expression in int but the return value will be a boolean
            
            On the bases of that boolean value, if-else are triggered.

        
        */
        int age = 24;

        if (age> 18){
            System.out.println("Yes, boy you can drive.");
        }

        else{
            System.out.println("No, you can't drive yet.");
        }

        


    }
    
}
